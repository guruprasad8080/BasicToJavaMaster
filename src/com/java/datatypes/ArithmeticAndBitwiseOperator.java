package com.java.datatypes;

public class ArithmeticAndBitwiseOperator {
    public static void main(String[] args) {

        System.out.println("Arithmetic Operators:");
        int a= 5+3;     // result is 8
        int b= 10-4;    // result is 6
        int c=6*7;      // result is 42
        float d=10/3;   // result is 3 (integer division)
        double e=10.0/3.0 ; // result is 3.333.. (floating division)
        int f=10%3;     //result is 1


        a++;    //post increment
        ++a;    //pre increment
        a--;    //post decrement
        --a;    //pre decrement

        System.out.println("Bitwise Operator: ");
        int val=2;

        System.out.println();

    }
}
